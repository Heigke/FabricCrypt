"""DS-N12: Online STDP-based adaptive filter on MIT-BIH ECG anomaly detection.

Streams MIT-BIH at 360 Hz; LIF input encoders convert ECG amplitude to spikes
(positive-amp and negative-amp neurons), driving N=64 NS-RAM cells (surrogate-
based snapback dynamics). Output spikes generated by Vb-threshold. STDP updates
W_in by Δt between presyn and postsyn spike (Hebbian: positive Δt → LTP,
negative → LTD). Online linear readout (NLMS) on V_b feature emits arrhythmia
probability per beat (PVC detection).

Conditions:
    NSRAM_STDP  : full NS-RAM dynamics + STDP plasticity
    NSRAM_FROZ  : NS-RAM dynamics, STDP disabled (W_in frozen at init)
    DIG_STDP    : pure digital LIF + same STDP (no analog snapback)
    RF_BASELINE : Random forest on QRS morphology features

Pre-registered gates:
    INFRA: 1 h ECG (360 Hz × 3600 s ≈ 1.3M samples) processed in <60 s wall.
    HYP PASS: NS-RAM PVC sensitivity > 0.85 AND specificity > 0.90.
    KILL: NS-RAM accuracy == DIG_STDP within 1pp → no analog advantage.

Output: results/DS_N12_stdp_ecg/{sensitivity_specificity.json, learning_curve.png,
ablation_table.md}
"""
