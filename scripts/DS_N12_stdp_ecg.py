STDP_A_PLUS  = 0.005
STDP_A_MINUS = 0.0055
STDP_TAU_PRE = 20.0  # ms
STDP_TAU_POST= 20.0
W_MAX = 1.5
W_MIN = 0.0

# NS-RAM cell
NSRAM_VG2_BIAS = 0.35
NSRAM_VG1_BIAS = 0.35
NSRAM_VD = 1.0
NSRAM_DT = 5e-7   # s per sub-step
NSRAM_SUB = 1
NSRAM_GIN = 0.4
NSRAM_C_B_F = 80e-15  # 80 fF
NSRAM_VB_SPIKE_THR = 0.06  # |Vb| above this → output spike

ENERGY_PER_NSRAM_SPIKE_FJ = 6.4
ENERGY_PER_DIG_SPIKE_FJ   = 50.0   # 32-bit add/MAC ~50fJ in 7nm
ENERGY_CNN_PER_BEAT_NJ    = 1500.0 # ~1.5 µJ for small CNN per beat (literature, conservative)

N_SYMS = {"N","L","R","e","j"}  # AAMI N
V_SYMS = {"V","E"}              # AAMI V (PVC)


# ============================================================
# Surrogate loader (4D NS-RAM)
# ============================================================
def load_surrogate(path):
    z = np.load(path)
    return {
        "I_d":   torch.tensor(z["Id"],    dtype=torch.float32, device=DEVICE),
        "I_ii":  torch.tensor(z["Iii"],   dtype=torch.float32, device=DEVICE),
        "I_leak":torch.tensor(z["Ileak"], dtype=torch.float32, device=DEVICE),
        "ax_VG1":torch.tensor(z["vg1_axis"], dtype=torch.float32, device=DEVICE),
        "ax_VG2":torch.tensor(z["vg2_axis"], dtype=torch.float32, device=DEVICE),
        "ax_Vd": torch.tensor(z["vd_axis"],  dtype=torch.float32, device=DEVICE),
        "ax_Vb": torch.tensor(z["vb_axis"],  dtype=torch.float32, device=DEVICE),
    }

def _frac(v, ax):
    n = ax.shape[0]
    i = torch.bucketize(v, ax) - 1
    i = i.clamp(0, n-2)
    lo, hi = ax[i], ax[i+1]
    t = ((v - lo)/(hi-lo)).clamp(0.0,1.0)
    return i, t

def query_surr(s, VG1, VG2, Vd, Vb):
    i0,t0 = _frac(VG1, s["ax_VG1"])
    i1,t1 = _frac(VG2, s["ax_VG2"])
    i2,t2 = _frac(Vd,  s["ax_Vd"])
    i3,t3 = _frac(Vb,  s["ax_Vb"])
    Id_t, Iii_t, Ilk_t = s["I_d"], s["I_ii"], s["I_leak"]
    Id_o = torch.zeros_like(VG1); Iii_o = torch.zeros_like(VG1); Ilk_o = torch.zeros_like(VG1)
    for a0 in (0,1):
        w0 = t0 if a0 else 1-t0; j0 = i0+a0
        for a1 in (0,1):
            w1 = t1 if a1 else 1-t1; j1 = i1+a1
            for a2 in (0,1):
                w2 = t2 if a2 else 1-t2; j2 = i2+a2
                for a3 in (0,1):
                    w3 = t3 if a3 else 1-t3; j3 = i3+a3
                    w = w0*w1*w2*w3
                    Id_o  = Id_o  + w*Id_t[j0,j1,j2,j3]
                    Iii_o = Iii_o + w*Iii_t[j0,j1,j2,j3]
                    Ilk_o = Ilk_o + w*Ilk_t[j0,j1,j2,j3]
    return Id_o, Iii_o, Ilk_o


# ============================================================
# Dataset: stream records, annotate beats
# ============================================================
def load_record(rec):
    r = wfdb.rdrecord(str(DATA/rec))
    a = wfdb.rdann(str(DATA/rec),'atr')
    sig_full = r.p_signal[:,0].astype(np.float32)
    # Downsample 3× by averaging
    L = (sig_full.shape[0] // DOWNSAMPLE) * DOWNSAMPLE
    sig = sig_full[:L].reshape(-1, DOWNSAMPLE).mean(axis=1)
    sig = (sig - sig.mean()) / (sig.std() + 1e-6)
    beats = []
    for samp, sym in zip(a.sample, a.symbol):
        s_ds = int(samp // DOWNSAMPLE)
        if sym in N_SYMS:
            beats.append((s_ds, 0))
        elif sym in V_SYMS:
            beats.append((s_ds, 1))
    return sig, beats


# ============================================================
# Input encoder: bipolar LIF → spike trains
# 2 input neurons per channel: positive, negative (rectified)
# Plus a derivative neuron (sensitive to QRS upstroke).
# Actually we use M=12 input neurons with random projection of (sig, dsig) → drive.
# ============================================================
M_IN = 16

def make_input_proj(seed, M=M_IN):
    rng = np.random.default_rng(seed)
    # Each input neuron sees a random linear combination of (sig, dsig, sig^2)
    P = rng.normal(0,1,size=(M,3)).astype(np.float32) * 0.7
    return torch.tensor(P, device=DEVICE)


# ============================================================
# NS-RAM + STDP online streamer
# ============================================================
class NSRAMOnline:
    def __init__(self, surr, M=M_IN, N=N_CELLS, seed=0, stdp_on=True):
        self.surr = surr
        g = torch.Generator(device='cpu').manual_seed(seed)
        # Init W_in (M_in, N) with sparse Gaussian
        self.W = (torch.randn(M, N, generator=g)*0.3 + 0.4).to(DEVICE)
        self.W.clamp_(W_MIN, W_MAX)
        self.Vb = torch.zeros(N, device=DEVICE)
        self.V_in_mem = torch.zeros(M, device=DEVICE)  # LIF inputs
        # STDP traces
        self.x_pre = torch.zeros(M, device=DEVICE)
        self.y_post = torch.zeros(N, device=DEVICE)
        self.stdp_on = stdp_on
        self.last_post_spike = torch.zeros(N, device=DEVICE) - 1e6
        self.last_pre_spike  = torch.zeros(M, device=DEVICE) - 1e6
        self.total_spikes = 0
        self.W_min = float(self.W.min().item()); self.W_max=float(self.W.max().item())

    def step(self, sig_t, dsig_t):
        """One sample step. Returns Vb tensor + output spike count this step."""
        # Encode input
        feat = torch.tensor([sig_t, dsig_t, sig_t*sig_t], device=DEVICE)
        drive_in = self.W.new_zeros(M_IN)  # we'll compute via projection externally
        return drive_in  # placeholder, actual stepping below


# We process a small WINDOW of samples per beat for efficiency,
# but the dynamics are still PER-SAMPLE.
def run_stream(records, surr, stdp_on=True, dig_only=False, seed=0,
               learn_readout=True, readout_state=None, log_curve=False,
               W_init=None):
    """Stream records sample-by-sample. Returns predictions + labels + diag."""
    rng = np.random.default_rng(seed)
    P = make_input_proj(seed)  # (M, 3)
    M = M_IN; N = N_CELLS

    if W_init is not None:
        W = W_init.clone().to(DEVICE)
    else:
        g = torch.Generator(device='cpu').manual_seed(seed)
        W = (torch.randn(M, N, generator=g)*0.25 + 0.35).to(DEVICE).clamp_(W_MIN, W_MAX)
    Vb = torch.zeros(N, device=DEVICE)
    Vmem_in = torch.zeros(M, device=DEVICE)   # LIF for inputs
    Vmem_dig = torch.zeros(N, device=DEVICE)  # LIF post-neurons (digital path)

    # STDP traces
    x_pre  = torch.zeros(M, device=DEVICE)
    y_post = torch.zeros(N, device=DEVICE)
    last_pre  = torch.full((M,), -1e6, device=DEVICE)
    last_post = torch.full((N,), -1e6, device=DEVICE)

    # Readout: per-beat logistic regression online (NLMS over Vb_chunk feature)
    FEAT_DIM = N + 2    # Vb (N) + [hr, rr_prev]
    if readout_state is None:
        w_r = torch.zeros(FEAT_DIM, device=DEVICE)
        b_r = torch.tensor(0.0, device=DEVICE)
    else:
        w_r, b_r = readout_state
    LR_R = 0.05

    all_preds = []; all_labels = []
    curve = []     # (n_beats_seen, running_acc)
    spike_count = 0
    spike_count_dig = 0
    t_start = time.time()

    sig_min = surr["ax_VG1"][0].item()
    sig_max = surr["ax_VG1"][-1].item()
    Vb_min  = surr["ax_Vb"][0].item()
    Vb_max  = surr["ax_Vb"][-1].item()
    _const_VG2 = torch.full((N,), NSRAM_VG2_BIAS, device=DEVICE)
    _const_Vd  = torch.full((N,), NSRAM_VD,       device=DEVICE)
    spike_count_t = torch.zeros((), device=DEVICE)
    spike_count_dig_t = torch.zeros((), device=DEVICE)

    correct = 0; total = 0

    for rec in records:
        sig, beats = load_record(rec)
        dsig = np.diff(sig, prepend=sig[0])
        # Find beat boundaries; process samples in between, classify at R-peak
        prev_R = 0; prev_rr = 250
        for bi, (samp, lbl) in enumerate(beats):
            # Tight window around R-peak: ±100 ms (240 ms total, ~29 samples at 120Hz)
            start = max(0, samp - int(0.1*FS_EFF))
            stop  = min(len(sig), samp + int(0.14*FS_EFF))
            seg_sig  = sig[start:stop]
            seg_dsig = dsig[start:stop]
            # Convert to torch on device
            seg_sig_t  = torch.tensor(seg_sig,  device=DEVICE)
            seg_dsig_t = torch.tensor(seg_dsig, device=DEVICE)

            Vb_accum = torch.zeros(N, device=DEVICE)
            n_acc = 0
            beat_amp = float(np.max(np.abs(seg_sig))) if len(seg_sig)>0 else 0.0
            dsig_max = float(np.max(seg_dsig)) if len(seg_dsig)>0 else 0.0
            rr_prev = float((samp - prev_R)/FS_EFF*1000.0)  # ms
            hr_inst = 60000.0/max(rr_prev, 200.0)

            # Vectorize: precompute drive matrix over full segment
            Lseg = len(seg_sig)
            if Lseg == 0:
                n_acc = 1; Vb_accum = Vb
            else:
                feat_in_mat = torch.stack([seg_sig_t, seg_dsig_t, seg_sig_t*seg_sig_t], dim=1)  # (L,3)
                drive_seq = feat_in_mat @ P.T   # (L, M)
                decay_pre  = math.exp(-LIF_DT/STDP_TAU_PRE)
                decay_post = math.exp(-LIF_DT/STDP_TAU_POST)
                alpha = LIF_DT/LIF_TAU

                # Sequential loop (Vb update inherently sequential)
                # Pre-allocate; keep ops on GPU, avoid python float()
                for k in range(Lseg):
                    drive = drive_seq[k]
                    Vmem_in = Vmem_in + (drive - Vmem_in)*alpha
                    pre_spk = (Vmem_in > LIF_THR).float()
                    Vmem_in = Vmem_in - pre_spk * LIF_THR
                    x_pre = x_pre * decay_pre + pre_spk
                    post_drive = pre_spk @ W   # (N,)

                    if not dig_only:
                        VG1 = (NSRAM_VG1_BIAS + NSRAM_GIN*post_drive).clamp(sig_min, sig_max)
                        # VG2, Vd are constant — precompute once outside k loop (done below via _const)
                        Vbc = Vb.clamp(Vb_min, Vb_max)
                        I_d, I_ii, I_leak = query_surr(surr, VG1, _const_VG2, _const_Vd, Vbc)
                        Vb = (Vb + NSRAM_DT * (I_ii - I_leak)/NSRAM_C_B_F).clamp(Vb_min, Vb_max)
                        post_spk = (Vb.abs() > NSRAM_VB_SPIKE_THR).float()
                    else:
                        Vmem_dig = Vmem_dig + (post_drive - Vmem_dig)*alpha
                        post_spk = (Vmem_dig > LIF_THR).float()
                        Vmem_dig = Vmem_dig - post_spk * LIF_THR
                        Vb = Vmem_dig

                    y_post = y_post * decay_post + post_spk

                    if stdp_on:
                        # Combined STDP update — always compute (no host sync)
                        W = W + STDP_A_PLUS * torch.outer(x_pre, post_spk) \
                              - STDP_A_MINUS * torch.outer(pre_spk, y_post)
                        W.clamp_(W_MIN, W_MAX)

                    if not dig_only:
                        spike_count_t += post_spk.sum()
                    else:
                        spike_count_dig_t += post_spk.sum()
                    Vb_accum = Vb_accum + Vb
                    n_acc += 1

            # Per-beat feature: use ONLY internal state (Vb) + RR interval.
            # Beat_amp/dsig_max would trivially solve the task and mask STDP effects;
            # exclude them so readout depends on substrate representation.
            Vb_feat = (Vb_accum / max(n_acc,1))
            scalar_feat = torch.tensor([hr_inst/100.0, rr_prev/1000.0], device=DEVICE)
            feat_full = torch.cat([Vb_feat, scalar_feat], dim=0)
            # Normalize
            feat_n = (feat_full - feat_full.mean())/(feat_full.std()+1e-6)
            logit = (w_r * feat_n).sum() + b_r
            prob = torch.sigmoid(logit).item()
            pred = 1 if prob > 0.5 else 0

            if learn_readout:
                # Online logistic SGD
                err = prob - lbl
                w_r = w_r - LR_R * err * feat_n
                b_r = b_r - LR_R * err

            all_preds.append(pred); all_labels.append(lbl)
            total += 1
            if pred == lbl: correct += 1
            if log_curve and total % 50 == 0:
                curve.append((total, correct/total))

            prev_R = samp
            prev_rr = rr_prev

    elapsed = time.time() - t_start
    spike_count += int(spike_count_t.item())
    spike_count_dig += int(spike_count_dig_t.item())
    return {
        "preds": np.asarray(all_preds, dtype=np.int64),
        "labels": np.asarray(all_labels, dtype=np.int64),
        "spike_count_nsram": spike_count,
        "spike_count_dig":   spike_count_dig,
        "elapsed_s": elapsed,
        "W_final": W.detach().cpu().numpy(),
        "readout": (w_r.detach(), b_r.detach()),
        "W_trained": W.detach(),
        "curve": curve,
    }


def metrics(preds, labels):
    tp = int(((preds==1)&(labels==1)).sum())
    fn = int(((preds==0)&(labels==1)).sum())
    tn = int(((preds==0)&(labels==0)).sum())
    fp = int(((preds==1)&(labels==0)).sum())
    sens = tp/max(tp+fn,1)
    spec = tn/max(tn+fp,1)
    acc  = (tp+tn)/max(tp+tn+fp+fn,1)
    return dict(sensitivity=sens, specificity=spec, accuracy=acc,
                tp=tp, fp=fp, tn=tn, fn=fn)


# ============================================================
# Random Forest baseline on simple QRS morphology features
# ============================================================
def rf_baseline(train_recs, test_recs):
    from sklearn.ensemble import RandomForestClassifier
    def feats_from(recs):
        Xs=[]; ys=[]
        for rec in recs:
            sig, beats = load_record(rec)
            dsig = np.diff(sig, prepend=sig[0])
            prev = 0
            for samp,lbl in beats:
                lo = max(0, samp-20); hi = min(len(sig), samp+20)
                w = sig[lo:hi]; dw = dsig[lo:hi]
                if len(w)<10: continue
                feat = [w.mean(),w.std(),w.min(),w.max(),
                        dw.mean(),dw.std(),dw.min(),dw.max(),
                        (samp-prev)/FS_EFF, len(w)]
                Xs.append(feat); ys.append(lbl); prev=samp
        return np.asarray(Xs), np.asarray(ys)
    Xtr,ytr = feats_from(train_recs)
    Xte,yte = feats_from(test_recs)
    t0=time.time()
    clf = RandomForestClassifier(n_estimators=80, max_depth=12, random_state=0, n_jobs=2)
    clf.fit(Xtr,ytr)
    pred = clf.predict(Xte)
    return metrics(pred, yte), time.time()-t0


